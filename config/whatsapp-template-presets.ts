/**
 * Get preset for a specific template type and style
 */
export function getTemplatePreset(
  type: WhatsAppTemplateType,
  style: "style1" | "style2" | "style3"
): WhatsAppTemplatePreset | undefined {
  return WHATSAPP_TEMPLATE_PRESETS.find(
    (preset) => preset.type === type && preset.style === style
  );
}

/**
 * Get all presets for a specific template type
 */
export function getTemplatePresetsByType(
  type: WhatsAppTemplateType
): WhatsAppTemplatePreset[] {
  return WHATSAPP_TEMPLATE_PRESETS.filter((preset) => preset.type === type);
}
